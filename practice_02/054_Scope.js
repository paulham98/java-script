let resultSubtraction;
function subtraction(a, b) {
    resultSubtraction = a - b;
}

subtraction(100, 50);
console.log(resultSubtraction);

function multiple(a, b) {
    const result = a * b;
    return result;
}

const r = multiple(10, 10);
console.log(r);

function sum(a, b) {
    const resultSum = a + b;
}

sum(20, 30);
console.log(resultSum);